package variablesAndConstants;

public class DeclaringVarsWithValues {

	public static void main(String[] args) {
		
		byte bit = 127;
		short valor = 254;
		int quantidade = 1000;
		long identificador = 10000l; // Ao atribuir um valor para uma variável do tipo long, no final do valor deve ser acrescentada a letra l minúscula, para indicar que se trata de um dado do tipo int.
		float altura = 25.4f; // Ao atribuir um valor para uma variável do tipo float, as casas decimais devem ser informadas, mesmo que seja zero e no final deve ser acrescentada a letra f minúscula, para diferenciar de um dado do tipo double.
		double area = 45.4567;
		char tipo = 'A';
		boolean resposta = true;
		String palavra = "Generation Brasil";

	}

}
