/*
 * - No Java a declaração de uma constante é bem parecida com a declaração de uma variável. 
 * Basta colocar a palavra reservada final antes do tipo da variável;
 * 
 * - Quando declaramos uma constante, o nome da constante deve ser escrito em letras 
 * maiúsculas e o valor da constante deve ser obrigatoriamente atribuído.
 */

package variablesAndConstants;

public class DeclaringConstants {

	public static void main(String[] args) {
		
		final byte BIT = 127;
		final short VALOR = 254;
		final int QAUNTIDADE = 1000;
		final long IDENTIFICADOR = 10000l;
		final float ALTURA = 25.4f; 
		final double AREA = 45.4567;
		final char TIPO = 'A';
		final boolean RESPOSTA = true;

	}

}
