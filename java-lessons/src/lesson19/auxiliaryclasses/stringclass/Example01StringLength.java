package lesson19.auxiliaryclasses.stringclass;

public class Example01StringLength {

	/**
	 * String.length()
	 * 
	 * → Returns the length of the String.
	 * 
	 */

	public static void main(String[] args) {

		String text = "Navy SEALs";

		System.out.println("=== String.length() ===\n");
		System.out.printf("→ 1) The length of the string '%s' is: %d.%n", text, text.length()); // 10

	}

}
