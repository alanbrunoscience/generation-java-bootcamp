package lesson02.variablesandconstants;

public class Example05Strings {

	public static void main(String[] args) {

		/*
		 * String Type
		 * 
		 * - Strings in Java are not treated as a mere sequence of characters. Strings are Objects or Instances 
		 * of the java.lang.String class, therefore, they must be declared and instantiated.
		 */
		
		// Variable declaration
		String phrase = "Hello! My name's Alan.";
		
		// Output
		System.out.println(phrase); // "Hello! My name's Alan."
		
	}

}
