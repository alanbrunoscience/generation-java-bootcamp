package lesson02.variablesandconstants;

public class Example01UninitializedVariables {

	public static void main(String[] args) {
		
		// Variables declaration
		
		// Integer Types
		byte bits; // Integer data type composed of 8 bits.
		short value; // Integer data type composed of 16 bits.
		int quantity; // Integer data type composed of 32 bits.
		long identifier; // Integer data type composed of 64 bits.
		
		// Real Types
		float height; // 32-bit single-precision floating-point data type.
		double area; // 64-bit double-precision floating point data type.
		
		// Non-numerical types
		char character; // Data type composed of a single 16-bit Unicode character.
		boolean answer; // Data type composed of only two possible values: 'true' and 'false'.
		
		// When we declare a variable without a value, the value of the variable must be assigned later.
		
	}

}
