package lesson02.variablesandconstants;

public class Example03MultipleVariablesDeclaration {

	public static void main(String[] args) {
		
		// Variables declaration
		
		// Integer Types
		byte bit1, bit2;
		short value1, value2;
		int quantity, age;
		long identifier, code;
		
		// Real Types
		float height, weight;
		double area, angle;
		
		// Non-numerical types
		char type, item;
		boolean answer1, answer2;
		
		// When we declare multiple variables without value, the value of these variables must be assigned later.

	}

}
